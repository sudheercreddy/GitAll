package Assigmenet;

public class vowelsWhile {

	public static void main(String[] args) {
		
		
		char j = 97;
		while(j<=122) {
			
			switch (j) {
			case 'a': {
				System.out.println(j);
				break;
			}
			case 'e': {
				System.out.println(j);
				break;
			}
			case 'i': {
				System.out.println(j);
				break;
			}
			case 'o': {
				System.out.println(j);
				break;
			}
			case 'u': {
				System.out.println(j);
				break;
			}
			default:
				break;
				
			}
			j++;
		}
		}

	}


