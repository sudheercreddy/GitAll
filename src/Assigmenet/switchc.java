package Assigmenet;

public class switchc {

	public static void main(String[] args) {
		
		String browser = "chrome";
		switch (browser) {
		case "chrome":
			System.out.println("launch chrome");
			break;
		case "firefox":
			System.out.println("launch firefox");
			break;
		case "edge":
			System.out.println("launch edge");
			break;

		default:
			break;
		}

	}

}
